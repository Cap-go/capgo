CREATE OR REPLACE FUNCTION public.bind_creating_apikey_to_org_on_create()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = ''
AS $$
DECLARE
  api_key_text text;
  api_key public.apikeys%ROWTYPE;
  org_super_admin_role_id uuid;
BEGIN
  SELECT public.get_apikey_header() INTO api_key_text;
  IF api_key_text IS NULL THEN
    RETURN NEW;
  END IF;

  SELECT *
  INTO api_key
  FROM public.find_apikey_by_value(api_key_text)
  LIMIT 1;

  IF api_key.id IS NULL
    OR public.is_apikey_expired(api_key.expires_at)
    OR api_key.user_id IS DISTINCT FROM NEW.created_by
  THEN
    RETURN NEW;
  END IF;

  IF NOT EXISTS (
    SELECT 1
    FROM public.apikey_global_permissions AS agp
    WHERE agp.apikey_rbac_id = api_key.rbac_id
      AND agp.permission_key = public.rbac_perm_org_create()
  )
  OR NOT public.apikey_has_current_org_create_capability(api_key.rbac_id) THEN
    RETURN NEW;
  END IF;

  SELECT roles.id
  INTO org_super_admin_role_id
  FROM public.roles
  WHERE roles.name = public.rbac_role_org_super_admin()
    AND roles.scope_type = public.rbac_scope_org()
  LIMIT 1;

  IF org_super_admin_role_id IS NULL THEN
    RAISE EXCEPTION 'org_super_admin role not found';
  END IF;

  INSERT INTO public.role_bindings (
    principal_type,
    principal_id,
    role_id,
    scope_type,
    org_id,
    granted_by,
    granted_at,
    reason,
    is_direct
  )
  VALUES (
    public.rbac_principal_apikey(),
    api_key.rbac_id,
    org_super_admin_role_id,
    public.rbac_scope_org(),
    NEW.id,
    NEW.created_by,
    pg_catalog.now(),
    'Auto-granted to API key on org creation',
    true
  )
  ON CONFLICT DO NOTHING;

  RETURN NEW;
END;
$$;

ALTER FUNCTION public.bind_creating_apikey_to_org_on_create() OWNER TO postgres;
REVOKE ALL ON FUNCTION public.bind_creating_apikey_to_org_on_create() FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.bind_creating_apikey_to_org_on_create() TO service_role;

DROP TRIGGER IF EXISTS bind_creating_apikey_to_org_on_create ON public.orgs;
CREATE TRIGGER bind_creating_apikey_to_org_on_create
AFTER INSERT ON public.orgs
FOR EACH ROW
EXECUTE FUNCTION public.bind_creating_apikey_to_org_on_create();

DROP POLICY IF EXISTS "Allow insert org for user" ON public.orgs;
DROP POLICY IF EXISTS "Allow insert org for apikey or user" ON public.orgs;
DROP POLICY IF EXISTS "Allow insert org for user or apikey with org.create" ON public.orgs;
CREATE POLICY "Allow insert org for user"
ON public.orgs
FOR INSERT
TO authenticated
WITH CHECK (
  created_by = (SELECT public.get_identity_for_apikey_creation())
);
