CREATE OR REPLACE FUNCTION public.rbac_perm_org_create()
RETURNS text
LANGUAGE sql
IMMUTABLE
SET search_path = ''
AS $$
  SELECT 'org.create'::text
$$;

ALTER FUNCTION public.rbac_perm_org_create() OWNER TO postgres;
REVOKE ALL ON FUNCTION public.rbac_perm_org_create() FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.rbac_perm_org_create() TO anon;
GRANT EXECUTE ON FUNCTION public.rbac_perm_org_create() TO authenticated;
GRANT EXECUTE ON FUNCTION public.rbac_perm_org_create() TO service_role;

COMMENT ON FUNCTION public.rbac_perm_org_create() IS
  'Global API-key permission for creating a new organization before an org-scoped RBAC binding can exist.';

CREATE TABLE IF NOT EXISTS public.apikey_global_permissions (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  apikey_rbac_id uuid NOT NULL REFERENCES public.apikeys(rbac_id) ON DELETE CASCADE,
  permission_key text NOT NULL,
  created_at timestamp with time zone DEFAULT now() NOT NULL,
  granted_by uuid REFERENCES public.users(id) ON DELETE SET NULL,
  reason text,
  CONSTRAINT apikey_global_permissions_permission_key_not_empty CHECK (permission_key <> ''),
  CONSTRAINT apikey_global_permissions_rbac_permission_unique UNIQUE (apikey_rbac_id, permission_key)
);

ALTER TABLE public.apikey_global_permissions OWNER TO postgres;
ALTER TABLE public.apikey_global_permissions ENABLE ROW LEVEL SECURITY;

REVOKE ALL ON TABLE public.apikey_global_permissions FROM PUBLIC;
REVOKE ALL ON TABLE public.apikey_global_permissions FROM anon;
