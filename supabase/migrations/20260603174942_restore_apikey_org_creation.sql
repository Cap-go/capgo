COMMENT ON TABLE public.apikey_global_permissions IS
  'Global permissions for API keys where no org/app/channel target exists yet. Currently used to grandfather org creation for existing write-capable keys without granting it to future keys by default.';

INSERT INTO public.apikey_global_permissions (
  apikey_rbac_id,
  permission_key,
  granted_by,
  reason
)
SELECT
  apikeys.rbac_id,
  public.rbac_perm_org_create(),
  apikeys.user_id,
  'Backfilled for org-scoped write-capable API keys that existed before org.create became explicit'
FROM public.apikeys
WHERE EXISTS (
  SELECT 1
  FROM public.role_bindings
  JOIN public.roles ON public.roles.id = public.role_bindings.role_id
  WHERE public.role_bindings.principal_type = public.rbac_principal_apikey()
    AND public.role_bindings.principal_id = apikeys.rbac_id
    AND public.role_bindings.scope_type = public.rbac_scope_org()
    AND (
      public.role_bindings.expires_at IS NULL
      OR public.role_bindings.expires_at > pg_catalog.now()
    )
    AND public.roles.name IN (
      public.rbac_role_org_super_admin(),
      public.rbac_role_org_admin()
    )
)
ON CONFLICT (apikey_rbac_id, permission_key) DO NOTHING;

CREATE OR REPLACE FUNCTION public.apikey_has_current_org_create_capability(
  p_apikey_rbac_id uuid
)
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = ''
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.role_bindings AS rb
    JOIN public.roles AS r ON r.id = rb.role_id
    WHERE rb.principal_type = public.rbac_principal_apikey()
      AND rb.principal_id = p_apikey_rbac_id
      AND rb.scope_type = public.rbac_scope_org()
      AND r.scope_type = public.rbac_scope_org()
      AND (
        rb.expires_at IS NULL
        OR rb.expires_at > pg_catalog.now()
      )
      AND r.name IN (
        public.rbac_role_org_super_admin(),
        public.rbac_role_org_admin()
      )
  )
$$;

ALTER FUNCTION public.apikey_has_current_org_create_capability(uuid) OWNER TO postgres;
REVOKE ALL ON FUNCTION public.apikey_has_current_org_create_capability(uuid) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.apikey_has_current_org_create_capability(uuid) TO service_role;

COMMENT ON FUNCTION public.apikey_has_current_org_create_capability(uuid) IS
  'Private helper ensuring org.create grants only remain effective while the API key still has a current org-scoped write-capable RBAC binding.';

CREATE OR REPLACE FUNCTION public.apikey_has_global_permission(
  p_apikey text,
  p_permission_key text
)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = ''
AS $$
DECLARE
  v_api_key public.apikeys%ROWTYPE;
BEGIN
  IF p_apikey IS NULL OR p_apikey = '' OR p_permission_key IS NULL OR p_permission_key = '' THEN
    RETURN false;
  END IF;

  SELECT *
  INTO v_api_key
  FROM public.find_apikey_by_value(p_apikey)
  LIMIT 1;

  IF v_api_key.id IS NULL OR public.is_apikey_expired(v_api_key.expires_at) THEN
    RETURN false;
  END IF;

  IF NOT EXISTS (
    SELECT 1
    FROM public.apikey_global_permissions AS agp
    WHERE agp.apikey_rbac_id = v_api_key.rbac_id
      AND agp.permission_key = p_permission_key
  ) THEN
    RETURN false;
  END IF;

  IF p_permission_key = public.rbac_perm_org_create() THEN
    RETURN public.apikey_has_current_org_create_capability(v_api_key.rbac_id);
  END IF;

  RETURN true;
END;
$$;

ALTER FUNCTION public.apikey_has_global_permission(text, text) OWNER TO postgres;
REVOKE ALL ON FUNCTION public.apikey_has_global_permission(text, text) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.apikey_has_global_permission(text, text) TO service_role;

COMMENT ON FUNCTION public.apikey_has_global_permission(text, text) IS
  'Service-role helper that checks global API-key permissions such as org.create using the supplied key value, including hashed-key lookup.';

CREATE OR REPLACE FUNCTION public.get_identity_for_apikey_creation()
RETURNS uuid
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = ''
AS $$
DECLARE
  auth_uid uuid;
BEGIN
  SELECT auth.uid() INTO auth_uid;
  IF auth_uid IS NOT NULL THEN
    RETURN auth_uid;
  END IF;

  PERFORM public.pg_log('deny: APIKEY_CREATE_WITH_API_KEY_DISABLED', '{}'::jsonb);
  RETURN NULL;
END;
$$;

