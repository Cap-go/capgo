/**
 * Pure generator for a GitHub Actions workflow file that runs `capgo build
 * request` against credentials uploaded as repository secrets.
 *
 * No I/O — caller decides where to write the resulting file. This keeps the
 * generator trivially testable: feed it opts, get a `{ path, content }` back.
 *
 * v1 deliberately limited: GitHub-only (no GitLab YAML), single `workflow_dispatch`
 * trigger (no push/pr triggers), one platform per dispatch (no matrix), no
 * monorepo subdirectory handling. Each of these is a follow-up.
 */

export type PackageManager = 'bun' | 'npm' | 'pnpm' | 'yarn'

export type BuildScriptChoice
  = | { type: 'npm-script', name: string }
    | { type: 'custom', command: string }
    | { type: 'skip' }

export interface WorkflowGeneratorOpts {
  /** The app's bundle ID, e.g. com.example.app */
  appId: string
  /** Default platform when the user dispatches the workflow without overriding the input. */
  defaultPlatform: 'ios' | 'android'
  /** Detected via getPMAndCommand(). Drives setup actions and the `npx`/`bunx`/etc runner. */
  packageManager: PackageManager
  /** What runs to produce the web bundle before `capgo build request` fires. */
  buildScript: BuildScriptChoice
  /** Exact secret names that were pushed to GitHub (drives the env: block). */
  secretKeys: string[]
}

export interface GeneratedWorkflow {
  /** Relative path inside the repo where the file should live. */
  path: string
  /** Full file content, ready to write. */
  content: string
}

/** Default path inside the repo. */
export const WORKFLOW_PATH = '.github/workflows/capgo-build.yml'

/**
 * Build the GitHub Actions workflow YAML.
 *
 * The shape is intentionally simple: one job, one platform per dispatch,
 * `workflow_dispatch` trigger only. Power users can fork it after the fact.
 */
export function generateWorkflow(opts: WorkflowGeneratorOpts): GeneratedWorkflow {
  const lines: string[] = []

  lines.push('name: Capgo native build')
  lines.push('')
  lines.push('# Generated by `capgo build init`. Safe to edit — see')
  lines.push('# https://capgo.app/docs/cli/cloud-build/ for the underlying CLI flags.')
  lines.push('')
  lines.push('on:')
  lines.push('  workflow_dispatch:')
  lines.push('    inputs:')
  lines.push('      platform:')
  lines.push(`        description: 'Platform to build'`)
  lines.push('        type: choice')
  lines.push('        options:')
  lines.push('          - ios')
  lines.push('          - android')
  lines.push(`        default: ${opts.defaultPlatform}`)
  lines.push('      build_mode:')
  lines.push(`        description: 'Build mode'`)
  lines.push('        type: choice')
  lines.push('        options:')
  lines.push('          - release')
  lines.push('          - debug')
  lines.push(`        default: release`)
  lines.push('')
  lines.push('jobs:')
  lines.push('  build:')
  lines.push('    runs-on: ubuntu-latest')
  lines.push('    steps:')
  lines.push('      - uses: actions/checkout@v4')

  // PM setup — bun and pnpm need TWO actions (the package manager + Node).
  // Per Capgo convention, bun runs alongside Node so build scripts that shell out
  // to `node` directly or rely on native binaries that resolve through Node still
  // work; bun's Node compat isn't perfect.
  appendPackageManagerSetup(lines, opts.packageManager)

  // Install dependencies — only if there's any chance of needing them. Even when
  // the user picked "skip build", they may still call npm scripts from
  // postinstall hooks or use `npx`; keeping the install step is the safe default.
  lines.push('')
  lines.push('      - name: Install dependencies')
  lines.push(`        run: ${installCommand(opts.packageManager)}`)

  // Web build — only if the user didn't pick "skip".
  if (opts.buildScript.type !== 'skip') {
    lines.push('')
    lines.push('      - name: Build web assets')
    appendRunBlock(lines, buildCommand(opts.packageManager, opts.buildScript))
  }

  // Capgo cloud build request. The `${{ ... }}` syntax below is GitHub Actions
  // expression syntax, not a JS template literal — it must be emitted verbatim
  // into the YAML. eslint's `no-template-curly-in-string` doesn't understand
  // that and would otherwise flag every single line here incorrectly.
  lines.push('')
  lines.push('      - name: Capgo native build')
  lines.push('        run: |')
  lines.push(`          ${runnerCommand(opts.packageManager)} @capgo/cli@latest build request ${opts.appId} \\`)
  // eslint-disable-next-line no-template-curly-in-string
  lines.push('            --platform ${{ inputs.platform }} \\')
  // eslint-disable-next-line no-template-curly-in-string
  lines.push('            --build-mode ${{ inputs.build_mode }} \\')
  // eslint-disable-next-line no-template-curly-in-string
  lines.push('            --apikey ${{ secrets.CAPGO_TOKEN }} \\')
  lines.push('            --output-upload --output-record /tmp/capgo-build.json')

  // The env: block forwards every secret we just pushed to GitHub. We enumerate
  // exactly the keys that the caller declares were pushed — no drift between
  // what we set and what we reference.
  if (opts.secretKeys.length > 0) {
    lines.push('        env:')
    for (const key of opts.secretKeys)
      lines.push(`          ${key}: \${{ secrets.${key} }}`)
  }

  // Artifact URL → step summary. Reads the JSON record `--output-record` wrote;
  // graceful no-op if the build didn't produce a URL (e.g. --output-upload was
  // disabled out-of-band by editing the YAML).
  lines.push('')
  lines.push('      - name: Surface artifact URL')
  lines.push('        if: success()')
  lines.push('        run: |')
  lines.push(`          URL=$(${runnerCommand(opts.packageManager)} @capgo/cli@latest build last-output --path /tmp/capgo-build.json --field outputUrl)`)
  lines.push('          if [ -n "$URL" ]; then')
  lines.push('            {')
  lines.push('              echo "### Capgo build ready"')
  lines.push('              echo ""')
  lines.push('              echo "- Artifact: $URL"')
  lines.push('            } >> "$GITHUB_STEP_SUMMARY"')
  lines.push('          fi')

  lines.push('')

  return { path: WORKFLOW_PATH, content: lines.join('\n') }
}

function appendPackageManagerSetup(lines: string[], pm: PackageManager): void {
  // Capgo convention: bun runs alongside Node. This matches the explicit
  // request from the maintainer to always include actions/setup-node even when
  // bun is the primary package manager.
  if (pm === 'bun') {
    lines.push('')
    lines.push('      - uses: oven-sh/setup-bun@v2')
    lines.push('        with:')
    lines.push('          bun-version: latest')
    lines.push('      - uses: actions/setup-node@v4')
    lines.push('        with:')
    lines.push(`          node-version: '20'`)
    return
  }

  if (pm === 'pnpm') {
    lines.push('')
    lines.push('      - uses: pnpm/action-setup@v4')
    lines.push('        with:')
    lines.push('          version: latest')
    lines.push('      - uses: actions/setup-node@v4')
    lines.push('        with:')
    lines.push(`          node-version: '20'`)
    lines.push(`          cache: 'pnpm'`)
    return
  }

  if (pm === 'yarn') {
    lines.push('')
    lines.push('      - uses: actions/setup-node@v4')
    lines.push('        with:')
    lines.push(`          node-version: '20'`)
    lines.push(`          cache: 'yarn'`)
    return
  }

  // npm
  lines.push('')
  lines.push('      - uses: actions/setup-node@v4')
  lines.push('        with:')
  lines.push(`          node-version: '20'`)
  lines.push(`          cache: 'npm'`)
}

function installCommand(pm: PackageManager): string {
  switch (pm) {
    case 'bun':
      return 'bun install --frozen-lockfile'
    case 'npm':
      return 'npm ci'
    case 'pnpm':
      return 'pnpm install --frozen-lockfile'
    case 'yarn':
      return 'yarn install --frozen-lockfile'
  }
}

function appendRunBlock(lines: string[], command: string): void {
  lines.push('        run: |')
  for (const line of command.replace(/\r\n/g, '\n').replace(/\r/g, '\n').split('\n'))
    lines.push(`          ${line}`)
}

function buildCommand(pm: PackageManager, choice: BuildScriptChoice): string {
  if (choice.type === 'custom')
    return choice.command
  if (choice.type === 'skip')
    return '# build step skipped — user opted out at wizard time'

  const scriptName = choice.name
  switch (pm) {
    case 'bun':
      return `bun run ${scriptName}`
    case 'npm':
      return `npm run ${scriptName}`
    case 'pnpm':
      return `pnpm run ${scriptName}`
    case 'yarn':
      return `yarn ${scriptName}`
  }
}

function runnerCommand(pm: PackageManager): string {
  switch (pm) {
    case 'bun':
      return 'bunx'
    case 'npm':
      return 'npx'
    case 'pnpm':
      return 'pnpm dlx'
    case 'yarn':
      return 'npx'
  }
}
